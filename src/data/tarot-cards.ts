const SUIT_META: Record<Exclude<TarotSuit, "major">, { name: string; nameEn: string }> = {
  wands: { name: "权杖", nameEn: "Wands" },
  cups: { name: "圣杯", nameEn: "Cups" },
  swords: { name: "宝剑", nameEn: "Swords" },
  pentacles: { name: "星币", nameEn: "Pentacles" },
};

function buildDeck(): TarotCardData[] {
  const deck: TarotCardData[] = [];

  majors.forEach((major, i) => {
    deck.push({
      id: `major-${i}`,
      index: i,
      name: major.name,
      nameEn: major.nameEn,
      suit: "major",
      number: i,
      label: ROMAN[i],
      uprightKeywords: major.upright,
      reversedKeywords: major.reversed,
      essence: major.essence,
    });
  });

  (Object.keys(minorData) as Array<Exclude<TarotSuit, "major">>).forEach(
    (suit, suitIndex) => {
      const meta = SUIT_META[suit];
      minorData[suit].forEach((entry, rank) => {
        const index = 22 + suitIndex * 14 + rank;
        deck.push({
          id: `${suit}-${rank + 1}`,
          index,
          name: `${meta.name}${RANK_NAMES[rank]}`,
          nameEn: `${RANK_EN[rank]} of ${meta.nameEn}`,
          suit,
          number: rank + 1,
          label: `${RANK_NAMES[rank]} · ${meta.name}`,
          uprightKeywords: entry[0],
          reversedKeywords: entry[1],
          essence: entry[2],
        });
      });
    },
  );

  return deck;
}

/** 完整 78 张塔罗牌 */
export const tarotDeck: TarotCardData[] = buildDeck();

const deckById = new Map(tarotDeck.map((card) => [card.id, card]));

export function getCardById(id: string): TarotCardData | undefined {
  return deckById.get(id);
}
